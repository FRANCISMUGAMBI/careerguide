            Choosing the wrong career path may be one of the big mastake you can do to your life. Please don't choose a
            course in the name "as long as am going to the campus" because you may end up wasting your time and
            resources studying a course that may never help you in life.Today I have a friend who dropped a course in
            college on her second year since it was not recognized and began another fresh course in another college.
            This is very painful, who can we brame? ofcourse we cannot blame the student because no one informed her
            about approval of courses. Be cautious before selecting a course!.
            
        <h2>These are some of the factors to consider before you choose a course</h2>.</p>
        <ol>
            <li>Approval/Recognition/Acredition of a course - Did you know that some universities and collenges are
                offering some courses that are not recognized by commission for university education. It sacks to study
                a course for 4 or 5 years and present your certificate when seaking a job only to realize that it's
                worthless. Universities are misleading students by offering some unapproved courses which they know well
                that graduantes may struggle alot to find a job in the market "in the name of money", some universities
                and collenges introduce more programmes in order to get more students and hence more money. Please take
                time to do research and find out whether the course you want to parsue is approved. </li>
            <li>Marketability/Job availability of the course - Choose a course that is in update to the current market,
                a course you are aware where and which industries it's applicable in life</li>
            <li>Your talent and abilities - choose a course that fits you, which you're best at, a course that you feel
                that you are comfortable in doing and are passionate in it.</li>
            <li>What Are Your Interests? - Think about the things that genuinely interest you when it comes to a work
                environment. Start by making a list of the things you do well. Think of how they can be applied to a
                career.</li>
            <li>The type of the course and its relation to other courses - Did you know that some of the universities
                are offerring the same course but with different title in order to get more students for more money? For
                example what is the essence of having Bachelor of Technology in Electrical Engineering and Bachelor of
                Science in Electrical Engineering? Don't be fooled the Bachelor of Technology course is meant to trap
                students who wanted to study Bachelor of Science course but did not attain the cutt-off points.
